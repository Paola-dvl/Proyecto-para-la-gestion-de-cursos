    public actualizarcurso(moduloadmin modAdmin, String codigo, String nombre, String creditos, String alumnos, String profesor) {
        initComponents();
        this.modAdmin = modAdmin;
        
        
        for (Profesor p : proyecto.shared.DataStorage.ListaProfe) {
        // Obtener el nombre y apellido del profesor
        String nombreProfesor = p.getNombre() + " " + p.getApellido();
        profesorq.addItem(nombreProfesor);
        
        
    }
        profesorq.setSelectedItem(profesor);
        
        this.codigo = codigo;
        this.nombre = nombre;
        this.creditos = creditos;
        this.alumnos = alumnos;
        this.profesor = profesor;
        
        codigoq.setText(codigo);
        nombreq.setText(nombre);
        creditosq.setText(creditos);
        alumnosq.setText(alumnos);
        
    }

