    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nuevoCodigo = codigoq.getText();
        String nuevoNombre = nombreq.getText();
        String nuevoCreditos = creditosq.getText();
        String nuevoAlumnos = alumnosq.getText();
        String nuevoProfesor = profesorq.getSelectedItem().toString();
        
        for (Curso c : proyecto.shared.DataStorage.ListaCurso) {
            if (c.getCodigo().equals(codigo)) {
                c.setCodigo(nuevoCodigo);
                c.setNombre(nuevoNombre);
                c.setCreditos(nuevoCreditos);
                c.setAlumnos(nuevoAlumnos);
                c.setProfesor(nuevoProfesor);
                break; 
            }
        }
        JOptionPane.showMessageDialog(this, "Curso actualizado con éxito");
        moduloadmin.actualizarTablaCurso(); 
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

